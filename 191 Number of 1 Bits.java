/*Solution: Bit manipulation, bit setter
* Time: O(32)
* Space: O(1)
*/
class Solution {
    // you need to treat n as an unsigned value
    public int hammingWeight(int n) {
        int count = 0;
        while (n != 0) {
            count += n & 1;
            n >>>= 1;
        }
        return count;
    }
}